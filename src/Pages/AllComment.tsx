import Navbar from "../Components/Navbar";
import { useEffect } from "react";
import { useCommentStore } from "../Store/useCommentStore";
import type { Comment } from "../types";

function AllComments() {
  const { comments, loading, fetchComments } = useCommentStore();

  useEffect(() => {
    fetchComments();
  }, [fetchComments]);

  if (loading) {
    return (
      <p className="text-center text-white py-10">
        Loading comments...
      </p>
    );
  }

  return (
    <div>
      <Navbar />
      <div className="w-[90%] mx-auto px-4 py-3">
        <h1 className="text-3xl font-bold text-white mb-6">All Comments</h1>

        <div className="grid grid-cols-1 sm:grid-cols-2 lg:grid-cols-3 gap-6">
          {comments.map((comment: Comment) => (
            <div
              key={comment.id}
              className="bg-[#1A1A1A] border border-gray-700 rounded-xl p-3 text-white"
            >
              {/* Star Rating */}
               <p className="text-yellow-400 text-lg">
              {"★".repeat(comment.rating)}{"☆".repeat(5 - comment.rating)}
            </p>


              {/* Summary */}
              <h4 className="text-md font-semibold mb-2">{comment.summary}</h4>

              {/* Description */}
              <p className="text-gray-300 mb-4">{comment.description}</p>

              <div className="flex">
                    <div className="flex justify-center">
                        <img
                        src={comment.img}
                        alt={comment.name}
                        className="w-20 h-20 rounded-full object-cover "
                        />
                    </div>
        
                    {/* Name + Location */}
                    <div>
                        <h3 className="text-lg font-semibold">{comment.name}</h3>
                        <p className="text-sm text-gray-400 ">
                            {comment.location}
                        </p>
                    </div>
              </div>

            </div>
          ))}
        </div>

      </div>
    </div>
  );
}

export default AllComments;
